#include <iostream>
using namespace std;
int main() // definition of the main function
{
   int a, b; // declaration of two variables of type int (integer numbers)
   a = 11;  // assigning value 11 to the variable a
   b = 33;
   std::cout << a << " + " << b << " = " << a + b << std::endl;  // 11 + 33 = 44
   return 0;
}